package com.example.securepasswords;

public class DataPassword {
        public String titulo;
        public String sitio;
        public String usuario;
        public String contrasena;
        public String notas;

        public DataPassword() {

        }

        public DataPassword(String titulo, String sitio, String usuario, String contrasena, String notas) {
            this.titulo = titulo;
            this.sitio = sitio;
            this.usuario = usuario;
            this.contrasena = contrasena;
            this.notas = notas;
        }

}
